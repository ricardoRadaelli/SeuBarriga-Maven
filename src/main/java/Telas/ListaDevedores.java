    private void listaDevedoresPorID(int idDevedor){
        DevedoresDAO devedorDAO = new DevedoresDAO();
        Devedores devedor = new Devedores();

        ClientesDAO clienteDAO = new ClientesDAO();
        ArrayList<Devedores> listaDevedores = devedorDAO.buscaDevedorPorId(idDevedor);
        model = (DefaultTableModel) jTDevedores.getModel();
        model.setNumRows(0);

        model.addRow(new Object[]{
            clienteDAO.buscaNomeCliente(listaDevedores.get(0).getIdCliente()),
            listaDevedores.get(0).getNomeDevedor(),
            listaDevedores.get(0).getCpfcnpj(),
            listaDevedores.get(0).getSaldoDevedor(),
            "Ação de Cobrança"
        });
        
        jTDevedores.setRowSelectionInterval(0, 0);
        // Configuração do renderizador personalizado para a coluna do botão
        jTDevedores.getColumn("Ação de Cobrança").setCellRenderer(new ListaDevedores.BotaoRenderer());

        // Configuração do editor de célula para a coluna do botão
        jTDevedores.getColumn("Ação de Cobrança").setCellEditor(new ListaDevedores.BotaoEditor());
    }
    
    private void pesquisarEnderecos(String nomeCliente, String cpfcnpj){
        DevedoresDAO devedorDAO = new DevedoresDAO();
        ClientesDAO clienteDAO = new ClientesDAO();
        int idCliente = clienteDAO.buscaIdClientePorCPF(cpfcnpj);
        int idDevedor = devedorDAO.buscaIdDevedor(cpfcnpj);
        
        EnderecosDAO endDAO = new EnderecosDAO();
        Enderecos end = new Enderecos();
        
        ArrayList<Enderecos> listaEnd = endDAO.buscaEndDevedor(idDevedor);
        modelEnderecos = (DefaultTableModel) jTEnderecos.getModel();
        modelEnderecos.setNumRows(0);
        for (int i = 0; i < listaEnd.size(); i++) {
            modelEnderecos.addRow(new Object[]{
                clienteDAO.buscaNomeCliente(listaEnd.get(i).getIdCliente()),
                listaEnd.get(i).getRua(),
                listaEnd.get(i).getNumero(),
                listaEnd.get(i).getComplemento(),
                listaEnd.get(i).getBairro(),
                listaEnd.get(i).getCidade()
            });
        }
    }
    
    private void pesquisarTelefones(String nomeCliente, String cpfcnpj){
        DevedoresDAO devedorDAO = new DevedoresDAO();
        ClientesDAO clienteDAO = new ClientesDAO();
        int idCliente = clienteDAO.buscaIdClientePorCPF(cpfcnpj);
        int idDevedor = devedorDAO.buscaIdDevedor(cpfcnpj);
        
        TelefonesDAO telDAO = new TelefonesDAO();
        Telefones tel = new Telefones();
        
        ArrayList<Telefones> listaTel = telDAO.buscaTelefoneDevedor(idDevedor);
        modelTelefones = (DefaultTableModel) jTTelefones.getModel();
        modelTelefones.setNumRows(0);
        for (int i = 0; i < listaTel.size(); i++) {
            modelTelefones.addRow(new Object[]{
                clienteDAO.buscaNomeCliente(listaTel.get(i).getIdCliente()),
                listaTel.get(i).getTelefone(),
                listaTel.get(i).getEmail()
            });
        }
    }
    
