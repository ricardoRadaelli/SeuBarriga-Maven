    // Editor de célula personalizado para lidar com o evento de clique do botão
    class BotaoEditor extends AbstractCellEditor implements TableCellEditor {
        private JButton button;

        public BotaoEditor() {
            button = new JButton();
            button.setOpaque(true);

            // Adiciona um ActionListener ao botão
            button.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    ClientesDAO cliDAO = new ClientesDAO();
                    int idCliente = cliDAO.buscaIdClientePorCPF(jTDevedores.getValueAt(jTDevedores.getSelectedRow(), 1).toString());
                    
                    DevedoresDAO creDAO = new DevedoresDAO();
                    int idDevedor = creDAO.buscaIdDevedor(jTDevedores.getValueAt(jTDevedores.getSelectedRow(), 2).toString());
                    
                    ListaAcoesCobranca tela = new ListaAcoesCobranca(idCliente, idDevedor, retornaTela());
                    tela.setVisible(true);
                }
            });
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            return button;
        }

        @Override
        public Object getCellEditorValue() {
            return null;
        }    
    }
    
