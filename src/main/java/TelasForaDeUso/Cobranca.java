    private void jBGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGravarActionPerformed
        AcaoCobrancaDAO aDAO = new AcaoCobrancaDAO();
        AcaoCobranca acao = new AcaoCobranca();
        
        acao.setDataCobranca(converteDataParaBanco(jTFDataCobranca.getText()));
        acao.setTextoCobranca(jTATextoCobranca.getText());
        acao.setTipoCobranca(jCBTipoCobranca.getSelectedItem().toString());
        acao.setIdCliente(idCompra);
        acao.setIdUsuario(idUsuario);
        
        aDAO.insereAcao(acao);
        dispose();
    }//GEN-LAST:event_jBGravarActionPerformed

