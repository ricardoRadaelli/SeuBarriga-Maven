    private Date converteData(String data){
        java.util.Date converte = null;
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            converte = sdf.parse(data);
        } catch (ParseException ex) {
            Logger.getLogger(Cobranca.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return converte;
    }
    
    private void buscaDadosCompra(){
        DebitosDAO dDao = new DebitosDAO();
        ClientesDAO cliDAO = new ClientesDAO();
        DevedoresDAO devDAO = new DevedoresDAO();
        
        Debitos listaDebitos = new Debitos();
        ArrayList<Clientes> listaClientes = new ArrayList<Clientes>();
        ArrayList<Devedores> listaDevedores = new ArrayList<Devedores>();
        
        listaDebitos = dDao.buscaDebitosPorIdCompra(idCompra);
        Clientes cliente = cliDAO.buscaClientesPorId(listaDebitos.getIdCliente());
        listaDevedores = devDAO.buscaDevedorPorId(listaDebitos.getIdDevedor());
        
        jTFCPFCliente.setText(listaClientes.get(0).getCpfcnpj());
        jTFCliente.setText(listaClientes.get(0).getNomeCliente());
        jTFDevedor.setText(listaDevedores.get(0).getNomeDevedor());
        jTFCPFDevedor.setText(listaDevedores.get(0).getCpfcnpj());
        jTFDataCompra.setText(listaDebitos.getDataCompra().toString());
        jTFValor.setText(String.valueOf(listaDebitos.getValor()));
        
    }
    
    
