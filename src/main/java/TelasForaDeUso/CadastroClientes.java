    private void jTFCPFCNPJFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTFCPFCNPJFocusLost
        String texto = jTFCPFCNPJ.getText().replaceAll("[^\\d]", "");
        
        String mascara;
        if(texto.length() <=11){
            mascara = "###.###.###-##";
        }else
            mascara = "##.###.###/####-##";
        
        try{
            MaskFormatter mask = new MaskFormatter(mascara);
            mask.setValueContainsLiteralCharacters(false);
            jTFCPFCNPJ.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(mask));
            jTFCPFCNPJ.setText(texto);
        }catch(ParseException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTFCPFCNPJFocusLost
    
