    private void buscaDados(String cpfcnpj){
        ClientesDAO cDAO = new ClientesDAO();
        Clientes cliente = new Clientes();
        
        TelefonesDAO tdao = new TelefonesDAO();
        Telefones telefone = new Telefones();
        
        EnderecosDAO edao = new EnderecosDAO();
        Enderecos end = new Enderecos();
        
        ArrayList<Clientes> lista = cDAO.buscaClientesPorCPF(cpfcnpj);
        ArrayList<Telefones> listaTelefone = tdao.buscaTelefoneCliente(lista.get(0).getIdCliente());
        ArrayList<Enderecos> listaEnd = edao.buscaEndCliente(lista.get(0).getIdCliente());
        
        
        
        jTFBairro.setText(listaEnd.get(0).getBairro());
        jTFCEP.setText(listaEnd.get(0).getCep());
        jTFNumero.setText(listaEnd.get(0).getNumero());
        jTFComplemento.setText(listaEnd.get(0).getComplemento());
        jTFEndereco.setText(listaEnd.get(0).getRua());
        jTFCidade.setText(listaEnd.get(0).getCidade());
        jCBEstado.setSelectedItem(listaEnd.get(0).getEstado());
        
        
        
        jTAExtras.setText(lista.get(0).getInformacoesAdicionais());
        jTFCPFCNPJ.setText(lista.get(0).getCpfcnpj());
        jTFComissao.setText(String.valueOf(lista.get(0).getComissao()));
        jTFCustoDevedorNaoPago.setText(String.valueOf(lista.get(0).getCustoDevedorNaoPago()));
        jTFDiaReembolso.setText(String.valueOf(lista.get(0).getDiaReembolso()));
        jTFEmail.setText(listaTelefone.get(0).getEmail());
        jTFNome.setText(lista.get(0).getNomeCliente());
        jTFRazao.setText(lista.get(0).getRazao());
        jTFResponsavel.setText(lista.get(0).getResponsavel());
        jTFTelefone.setText(listaTelefone.get(0).getTelefone());
        jLValorEmCobranca.setText(String.valueOf(lista.get(0).getValorEmCobranca()));
    }
    
